# Exercise 004: Arithmetic Operators
# Create a system to convert meters in centimeters and in millimeters

meters = float(input('Enter to distance in meters: '))

print(f'This original distance in meters is {meters:.2f}')
print(f'This distance in centimeters is {meters * 100:.2f}')
print(f'This original distance in meters is {meters * 1000:.2f}')
