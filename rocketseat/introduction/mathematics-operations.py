# Sum
result = 5 + 3
print("Sum:", result)

# Subtraction
result = 5 - 3
print("Subtraction:", result)

# Multiplication
result = 5 * 3
print("Multiplication:", result)

# Division
result = 5 / 3
print("Division:", result)

# Integer Division
result = 5 // 3
print("Integer Division:", result)

# Modulus
result = 5 % 3
print("Modulus:", result)

# Exponentiation
result = 5 ** 3
print("Exponentiation:", result)

# Square Root
result = 5 ** 0.5
print("Square Root:", result)

# Using math module for more operations
from math import sqrt, pow, log, sin, cos, tan, factorial

# Square Root using math module
result = sqrt(25)
print("Square Root (math module):", result)

# Power using math module
result = pow(5, 3)
print("Power (math module):", result)

# Logarithm
result = log(100, 10)
print("Logarithm:", result)

# Trigonometric functions
angle = 45  # degrees
radians = angle * (3.14159265 / 180)  # convert to radians
print("Sine:", sin(radians))
print("Cosine:", cos(radians))
print("Tangent:", tan(radians))

# Factorial
result = factorial(5)
print("Factorial:", result)